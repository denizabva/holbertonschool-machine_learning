#!/usr/bin/env python3
"""
Function that concatenates two arrays.
"""


def cat_arrays(arr1, arr2):
    """
    Concatenates two arrays.

    Args:
        arr1 (list of int/float): first array
        arr2 (list of int/float): second array

    Returns:
        list: new concatenated array
    """
    return arr1 + arr2
